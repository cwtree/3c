package com.cwtree.bean;

public class Report {

	private String goodsId;
	
	private String goodsName;
	
	private String goodsKind;
	
	private String goodsCb;
	
	private String goodsPrice;
	
	private String dealNumber;
	
	private String liRun;
	
	private String dealDate;
	
	public String getGoodsId() {
		return goodsId;
	}

	public void setGoodsId(String goodsId) {
		this.goodsId = goodsId;
	}

	public String getGoodsName() {
		return goodsName;
	}

	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}

	public String getGoodsKind() {
		return goodsKind;
	}

	public void setGoodsKind(String goodsKind) {
		this.goodsKind = goodsKind;
	}

	public String getGoodsCb() {
		return goodsCb;
	}

	public void setGoodsCb(String goodsCb) {
		this.goodsCb = goodsCb;
	}

	public String getGoodsPrice() {
		return goodsPrice;
	}

	public void setGoodsPrice(String goodsPrice) {
		this.goodsPrice = goodsPrice;
	}

	public String getDealNumber() {
		return dealNumber;
	}

	public void setDealNumber(String dealNumber) {
		this.dealNumber = dealNumber;
	}

	public String getLiRun() {
		return liRun;
	}

	public void setLiRun(String liRun) {
		this.liRun = liRun;
	}

	public String getDealDate() {
		return dealDate;
	}

	public void setDealDate(String dealDate) {
		this.dealDate = dealDate;
	}

}



















